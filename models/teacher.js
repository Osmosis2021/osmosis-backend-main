const mongoose = require('mongoose');

// create Teacher Schema here
const TeacherSchema = new mongoose.Schema(
	{
		name: {
			type: String,
			required: true,
		},
		email: {
			type: String,
			required: true,
			unique: true,
		},
		password: {
			type: String,
			required: true,
		},
	},
	{
		timestamps: true,
		toJSON: {
			virtuals: true,
			transform: (_doc, ret) => {
				delete ret.password;
				return ret;
			},
		},
	}
);

// export Teacher Schema
const Teacher = mongoose.model('Teacher', TeacherSchema);
module.exports = Teacher;
